const useTokenData = (onSuccess, onError, selectedProjectTokenDataUrl) => {
    return useQuery(
        ["token-data", selectedProjectTokenDataUrl],
        fetchTokenData,
        {
            onSuccess,
            onError,
            select: (data: any) => {
                return data?.data
            },
            refetchOnWindowFocus: false,
            cacheTime: CACHE_TIME,
            staleTime: STALE_TIME,
            enabled: false, // Do not fetch data when component mounts
        }
    )
}

